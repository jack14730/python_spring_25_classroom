"""
_summary_ 
    Conversion used to calculate the amount of miles per gallon of gas
"""

# Variables
tank_size = 15  # Size of tank defined in gallons
distance = 600  # Gives defined distance covered in miles

# Calculate miles per gallon
mpg = distance / tank_size

# Print the result
print("Miles per gallon:", mpg)  # Gives you the miles per gallon
